                    <h3>Золотошукач</h3>
                    <p>Чи знаєш ти, як раніше добували золото? Золотоносний грунт промивали в ситі, пісок йшов, а крихти золота залишалися. Іноді траплялися й цілі злитки, які миттєво робили добувача багатієм. Твоє завдання - пошукати такі золоті крихти і злитки в нових підручниках. Все, що тебе зацікавить, - золото. Те, що не зачепить, - пісочок.</p>
                    <p>Золото - вельми корисна інформація. У підручниках вона неодмінно є. Пошукай її, - і тоді кожен урок буде захоплюючим, а домашні завдання не складуть жодних труднощів.</p>
                    <p>Пісочок теж знадобиться: з ним ти можеш тренувати волю і кожен день робити не менше двох нудних справ. Зможеш більше - взагалі герой, але два - обов'язково! І тоді твоя воля втягнутися в навчання буде зміцнюватись з кожним днем.</p>
