                    <h2>Що це за звір такий – шкільний психолог?</h2><hr><br>
                    <p>У наші часи майже у кожній школі є психолог. Але деякі учні про це навіть і не знають. А більшість тих, хто знають, все одно до них не ходять, навіть якщо і мають певні проблеми.</p>
                    <p>В деяких школах ставлення дітей до психологів дуже дивне. Особливо це стосується молодших класів. Так, якщо однокласник відвідує психолога, то його починають вважати ненормальним, божевільним, дражнити психом. Це тому, що діти мало обізнані у тому, чим займаються психологи, тому і видумують різноманітні міфи, які жодного відношення до реальності не мають!</p>
                    <p>То навіщо ж тоді все-таки потрібні шкільні психологи?</p>
                    <p>В чому полягає робота шкільного психолога та які її особливості?</p>
                    <p>Шкільні психологи відносяться до групи психологів-педіатрів. Але, на відміну від самих педіатрів, вони не займаються лікуванням дітей. Вони існують в основному для того, щоб вислухати проблему, дати пораду. Чи помічали ви, що коли комусь викажеш свої тривоги, то одразу й легше стає на душі. Для деяких роль такої собі “жилетки”, у яку можна виплакатись, виконують друзі чи батьки. Але ж бувають такі ситуації, про які соромно або незручно розповісти мамі, таткові, а подруга чи друг потрібної поради не дадуть, адже самі не мають достатнього життєвого досвіду.</p>
                    <p>В таких випадках допомагає психолог. Він у жодному разі не має права розголошувати інформацію, яку розповів учень. Лише у крайньому випадку може поговорити з батьками, та й то тільки з твого дозволу, або за твоїм проханням.</p>
